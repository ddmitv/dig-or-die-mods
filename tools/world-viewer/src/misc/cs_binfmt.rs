impl BinFmtReadable for Vec<String> {
    fn binfmt_read_from(reader: &mut CSBinReader) -> Result<Vec<String>> {
        // List<T> contains 3 serialized fields: T[] _items, int _size, int _version

        check_magic(reader, &magic::STRING_LIST).context("Invalid magic for \"List<String>\"")?;
        let list_size: i32 = reader.read_int()?;
        let _list_version: i32 = reader.read_int()?;

        const ARRAY_OF_STRINGS: u8 = 0x11;
        const RECORD_TYPE_STRING: u8 = 0x06;
        const ITEMS_OBJECT_ID: u32 = 0x02;

        let items_record_type = reader.read_byte()?;
        ensure!(items_record_type == ARRAY_OF_STRINGS, "_items record type mismatch: expected {ARRAY_OF_STRINGS:#02X}, got {items_record_type:#02X}");
        let items_object_id = reader.read_uint()?;
        ensure!(items_object_id == ITEMS_OBJECT_ID, "_items object ID mismatch: expected {ITEMS_OBJECT_ID:#02X}, got {items_object_id:#02X}");

        let list_capacity: i32 = reader.read_int()?; // length of _items field
        
        ensure!(list_size <= list_capacity, "Invalid List state: size ({list_size}) > capacity ({list_capacity})");
        
        // cannot trust list_length and list_capacity since they are from untrusted source and can cause OOM
        let mut result: Vec<String> = Vec::new();
        
        let mut expected_object_id: u32 = 3;
        for _ in 0..list_size {
            let record_type = reader.read_byte()?;
            ensure!(record_type == RECORD_TYPE_STRING, "Object record type mismatch: expected {RECORD_TYPE_STRING:#02X}, got {record_type:#02X}");
        
            let object_id = reader.read_uint()?;
            ensure!(object_id == expected_object_id, "String object ID mismatch: expected {expected_object_id:#02X}, got {object_id:#02X}");
        
            result.push(reader.read_string()?.to_owned());
            expected_object_id += 1;
        }
        const NULL_VALUE_ELEM: u8 = 0x0A;
        const ARRAY_FILLER_8B_ELEM: u8 = 0x0D;
        const ARRAY_FILLER_32B_ELEM: u8 = 0x0E;
        
        let expected_nulls = list_capacity - list_size;
        match expected_nulls {
            0 => (),
            1 => {
                check_magic(reader, &[NULL_VALUE_ELEM])?;
            },
            2 => {
                check_magic(reader, &[NULL_VALUE_ELEM])?;
                check_magic(reader, &[NULL_VALUE_ELEM])?;
            },
            3..=255 => {
                check_magic(reader, &[ARRAY_FILLER_8B_ELEM])?;
                let actual_nulls = reader.read_byte()?;
                ensure!(actual_nulls as i32 == expected_nulls, "Invalid number of null fillers: expected {expected_nulls}, got {actual_nulls}");
            },
            _ => {
                check_magic(reader, &[ARRAY_FILLER_32B_ELEM])?;
                let actual_nulls = reader.read_byte()?;
                ensure!(actual_nulls as i32 == expected_nulls, "Invalid number of null fillers: expected {expected_nulls}, got {actual_nulls}");
            }
        };
        check_message_end_tag(reader)?;
        return Ok(result);
    }
}
