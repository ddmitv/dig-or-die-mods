fn check_magic(reader: &mut CSBinReader, magic: &[u8]) -> Result<()> {
    let actual = reader.read_bytes(magic.len())?;
    if actual != magic {
        let first_diff_bytes = magic.iter()
            .zip(actual)
            .skip_while(|(exp, act)| exp == act)
            .map(|(exp, act)| {
                if exp == act {
                    format!("{:02X}", act)
                } else {
                    format!("*{:02X} ({:02X})", act, exp)
                } });
            
        let diff_pos = reader.pos() - first_diff_bytes.clone().count();
        let diff_str = first_diff_bytes.collect::<Vec<String>>().join(", ");
        bail!("Magic mismatch at {:#X}: [..., {}]", diff_pos, diff_str);
    }
    return Ok(());
}
fn check_message_end_tag(reader: &mut CSBinReader) -> Result<()> {
    let actual = reader.read_byte()?;
    if actual != magic::MESSAGE_END_TAG {
        bail!("BinaryFormatter's message end tag magic value mismatch at {:#X}: expected {:#02X}, got {:#02X}", reader.pos() - 1, magic::MESSAGE_END_TAG, actual);
    }
    return Ok(());
}

pub trait BinFmtReadable: Sized {
    fn binfmt_read_from(reader: &mut CSBinReader) -> Result<Self>;
}

macro_rules! binfmtreadable_impl {
    ($reader:ident, $read:block, $type:ty, $magic:expr, $name:literal) => {
        impl BinFmtReadable for $type {
            fn binfmt_read_from($reader: &mut CSBinReader) -> Result<Self> {
                check_magic($reader, &$magic).context(concat!("Invalid magic for \"", $name, "\""))?;
                let result = $read;
                check_message_end_tag($reader).context(concat!("Invalid message end tag for \"", $name, "\""))?;
                return Ok(result);
            }
        }
    };
}

